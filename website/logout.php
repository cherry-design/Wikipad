    header("Location: ./");
}

///////////////////////////////////////////////////////////////////////////////
//                                                                           //
//            Функция обеспечивающая выход пользователя из системы           //
//                                                                           //
///////////////////////////////////////////////////////////////////////////////

function logout_user() {

    // Удаляем логин и пароль пользователя из Cookies
    setcookie("user_login", "");
    setcookie("user_password", "");
}

///////////////////////////////////////////////////////////////////////////////

// Обеспечиваем выход пользователя
logout_user();

// Перенаправляем пользователя на страницу с которой он пришел
header("Location: ".get_referer_link());

